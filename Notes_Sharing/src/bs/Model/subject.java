package bs.Model;

public class subject {
 private int sid;
 private int bid;
 private String sname;
 private String ssem;
 private String sdesc;
 private String snotes;
public String getSdesc() {
	return sdesc;
}
public void setSdesc(String sdesc) {
	this.sdesc = sdesc;
}
public String getSsem() {
	return ssem;
}
public void setSsem(String ssem) {
	this.ssem = ssem;
}
public String getSname() {
	return sname;
}
public void setSname(String sname) {
	this.sname = sname;
}
public int getBid() {
	return bid;
}
public void setBid(int bid) {
	this.bid = bid;
}
public int getSid() {
	return sid;
}
public void setSid(int sid) {
	this.sid = sid;
}
public String getSnotes() {
	return snotes;
}
public void setSnotes(String snotes) {
	this.snotes = snotes;
}
 
}
